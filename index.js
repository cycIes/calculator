function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log(add(1, 5));
console.log(subtract(1, 5));
console.log(multiply(2, 5));
console.log(divide(10, 5));
